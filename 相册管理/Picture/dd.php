	function SetRiLi($day,$w)
	{ 
		$w = ($w == 7) ? 0 : $w;
		$tab = '<table border="1">';
	 	$tab .= '<tr><th>星期日</th><th>星期一</th><th>星期二</th><th>星期三</th><th>星期四</th><th>星期五</th><th>星期六</th></tr>';

		if ($day > 31 || $day < 28 || $w > 7) { 
			echo '输入的天数或星期几不对，请重新输入';exit();
		}
		$row = ceil(($day + $w) / 7);
		$num = 1;
		for ($i = 0; $i < $row; $i++) { 
			$tab .= '<tr>';
			for ($j = 0; $j < $w; $j++) { 
				$tab .= '<td>&nbsp;</td>';
			}
			for ($k = 0; $k < 7 - $w; $k++) { 
				if ($num > $day) { 
					$tab .='<td>&nbsp;</td>';
				} else { 
					$tab .= "<td>$num</td>";
				}
				$num++;
			}
			$w = 0;
			$tab .='</tr>';
		}
		$tab .= '</table>';
		return $tab;
	}
