	function echoTab($colorNum)
	{
		$tab = '<table>';
			for($i = 1; $i <= 9; $i++){
				$tab .= '<tr>';
					for($j = 1; $j <= 9; $j++){
							$h = $colorNum;		
							$s = $i * 10 . '%';
							$l = $j * 10 . '%';
							
						$tab .= "<td style='background:hsl({$h},{$s},{$l});'></td>";

					}
				$tab .= '</tr>';
			}
		$tab .= '</table>';
		return $tab;
	}
